def normalize_months_text_to_list(text: str) -> list[int]:
    # приводим к нижнему регистру, заменяем точки/точки с запятой/слеши на запятую
    t = (text or "").strip().lower()
    if not t:
        return []

    # Разделители: запятая, пробелы, переносы, ; / |
    tokens = re.split(r"[,\s;/|]+", t)
    months: list[int] = []

    for tok in tokens:
        tok = tok.strip()
        if not tok:
            continue

        if tok.isdigit():
            m = int(tok)
            if 1 <= m <= 12:
                months.append(m)
            continue

        m = MONTH_MAP.get(tok)
        if m:
            months.append(m)

    # уникальные, в порядке возрастания (или оставь порядок ввода — как хочешь)
    months = sorted(set(months))
    return months


def months_list_to_canonical_text(months: list[int]) -> str:
    # "10,1,5" лучше хранить отсортированным: "1,5,10"
    mm = sorted({m for m in months if 1 <= m <= 12})
    return ",".join(str(m) for m in mm)
