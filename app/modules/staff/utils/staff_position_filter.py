import re


def _norm_pos(s: str | None) -> str:
    """Нормализация должности для сравнения (RU/KZ)."""
    if not s:
        return ""
    s = str(s).strip()
    # Excel бывает: ="..."
    if s.startswith('="') and s.endswith('"'):
        s = s[2:-1].strip()
    s = s.replace("\xa0", " ").strip().lower()
    # унифицируем тире/слэши/пробелы
    s = s.replace("–", "-").replace("—", "-")
    s = s.replace("\\", "/")
    s = re.sub(r"\s+", " ", s)
    return s


